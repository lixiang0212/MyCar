public class CarB {
    private int picId;
    private String name;

    public CarB(int picId, String name) {
        this.picId = picId;
        this.name = name;
    }

    public int getPicId() {
        return picId;
    }

    public void setPicId(int picId) {
        this.picId = picId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
